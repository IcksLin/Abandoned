#ifndef __ZF_DEVICE_IPS200_FB_HPP__
#define __ZF_DEVICE_IPS200_FB_HPP__

#include "zf_common_typedef.hpp"
#include <cstring>      // for memcpy
#include <algorithm>    // for std::min/std::max (可选，也可自己实现)

#define DEFAULT_PENCOLOR         (RGB565_RED)                          // 默认的画笔颜色
#define DEFAULT_BGCOLOR          (RGB565_WHITE)                        // 默认的背景颜色

#define FB_PATH                 "/dev/fb0"                             // framebuffer设备节点路径

//-------------------------------------------------------------------------------------------------------------------
// 类名         zf_device_ips200
// 说明         IPS200显示屏 framebuffer驱动C++封装类，240*320分辨率 RGB565格式
// 适用         LS2K0300平台，所有成员函数无ips200前缀，封装性强，调用简洁
//-------------------------------------------------------------------------------------------------------------------
class zf_device_ips200
{
private:
    uint16 pen_color;                          // 画笔颜色
    uint16 bg_color;                           // 背景颜色
    int width;                                 // 屏幕宽度
    int height;                                // 屏幕高度
    unsigned short *screen_base;                // 映射后的显存基地址
    unsigned short *buffer;                     // 后台缓冲区 (size = width * height * 2)
    int dirty_min_x, dirty_min_y;               // 脏区域左上角
    int dirty_max_x, dirty_max_y;               // 脏区域右下角

    // 私有辅助函数：标记单个点为脏
    inline void mark_dirty_point(int x, int y) {
        if (x < dirty_min_x) dirty_min_x = x;
        if (x > dirty_max_x) dirty_max_x = x;
        if (y < dirty_min_y) dirty_min_y = y;
        if (y > dirty_max_y) dirty_max_y = y;
    }

    // 私有辅助函数：标记矩形区域为脏
    inline void mark_dirty_rect(int x1, int y1, int x2, int y2) {
        if (x1 < dirty_min_x) dirty_min_x = x1;
        if (x2 > dirty_max_x) dirty_max_x = x2;
        if (y1 < dirty_min_y) dirty_min_y = y1;
        if (y2 > dirty_max_y) dirty_max_y = y2;
    }

public:
    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     无参构造函数
    // 参数说明     void
    // 返回参数     void
    // 使用示例     zf_device_ips200 lcd;
    // 备注信息     初始化默认画笔和背景颜色，显存地址置空，缓冲区置空，脏区域无效
    //-------------------------------------------------------------------------------------------------------------------
    zf_device_ips200(void);

    // 析构函数：释放缓冲区，解除显存映射
    ~zf_device_ips200();

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     清屏函数
    // 参数说明     void
    // 返回参数     void
    // 使用示例     ips200.clear();
    // 备注信息     将屏幕清空成背景颜色
    //-------------------------------------------------------------------------------------------------------------------
    void clear(void);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     屏幕填充函数
    // 参数说明     color           颜色格式 RGB565 或者可以使用 zf_common_font.h 内 rgb565_color_enum 枚举值或者自行写入
    // 返回参数     void
    // 使用示例     ips200.full(RGB565_BLACK);
    // 备注信息     将屏幕填充成指定颜色
    //-------------------------------------------------------------------------------------------------------------------
    void full(const uint16 color);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     画点函数
    // 参数说明     x               坐标x方向的点位置 [0, width-1]
    // 参数说明     y               坐标y方向的点位置 [0, height-1]
    // 参数说明     color           颜色格式 RGB565 或者可以使用 zf_common_font.h 内 rgb565_color_enum 枚举值或者自行写入
    // 返回参数     void
    // 使用示例     ips200.draw_point(10, 20, RGB565_BLUE);
    // 备注信息     在指定坐标绘制一个像素点，写入缓冲区并标记脏区域
    //-------------------------------------------------------------------------------------------------------------------
    void draw_point(uint16 x, uint16 y, const uint16 color);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     画线函数
    // 参数说明     x_start         坐标x方向的起点 [0, width-1]
    // 参数说明     y_start         坐标y方向的起点 [0, height-1]
    // 参数说明     x_end           坐标x方向的终点 [0, width-1]
    // 参数说明     y_end           坐标y方向的终点 [0, height-1]
    // 参数说明     color           颜色格式 RGB565 或者可以使用 zf_common_font.h 内 rgb565_color_enum 枚举值或者自行写入
    // 返回参数     void
    // 使用示例     ips200.draw_line(0, 0, 10, 10, RGB565_RED);
    // 备注信息     在指定两点之间绘制一条直线，内部调用 draw_point 实现
    //-------------------------------------------------------------------------------------------------------------------
    void draw_line(uint16 x_start, uint16 y_start, uint16 x_end, uint16 y_end, const uint16 color);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示单个字符
    // 参数说明     x               坐标x方向的起点 [0, width-1]
    // 参数说明     y               坐标y方向的起点 [0, height-1]
    // 参数说明     dat             需要显示的单个字符 数据类型 char
    // 返回参数     void
    // 使用示例     ips200.show_char(10, 20, 'A');
    // 备注信息     显示8x16的ASCII字符集，字符范围ASCII码32~127
    //-------------------------------------------------------------------------------------------------------------------
    void show_char(uint16 x, uint16 y, const char dat);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示字符串
    // 参数说明     x               坐标x方向的起点 [0, width-1]
    // 参数说明     y               坐标y方向的起点 [0, height-1]
    // 参数说明     dat             需要显示的字符串首地址 数据类型 const char[]
    // 返回参数     void
    // 使用示例     ips200.show_string(10, 20, "SEEKFREE");
    // 备注信息     连续显示8x16的ASCII字符，字符串以'\0'结尾
    //-------------------------------------------------------------------------------------------------------------------
    void show_string(uint16 x, uint16 y, const char dat[]);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示有符号整型数
    // 参数说明     x               坐标x方向的起点 [0, width-1]
    // 参数说明     y               坐标y方向的起点 [0, height-1]
    // 参数说明     dat             需要显示的变量 数据类型 int32
    // 参数说明     num             整数显示长度 最高支持10位
    // 返回参数     void
    // 使用示例     ips200.show_int(10, 20, -123, 3);
    // 备注信息     超出长度会显示低位数值，不足长度补空格，支持负数显示负号
    //-------------------------------------------------------------------------------------------------------------------
    void show_int(uint16 x, uint16 y, const int32 dat, uint8 num);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示无符号整型数
    // 参数说明     x               坐标x方向的起点 [0, width-1]
    // 参数说明     y               坐标y方向的起点 [0, height-1]
    // 参数说明     dat             需要显示的变量 数据类型 uint32
    // 参数说明     num             整数显示长度 最高支持10位
    // 返回参数     void
    // 使用示例     ips200.show_uint(10, 20, 123, 3);
    // 备注信息     超出长度会显示低位数值，不足长度补空格，无符号不显示负号
    //-------------------------------------------------------------------------------------------------------------------
    void show_uint(uint16 x, uint16 y, const uint32 dat, uint8 num);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示浮点数(去除整数部分无效的0)
    // 参数说明     x               坐标x方向的起点 参数范围 [0, width-1]
    // 参数说明     y               坐标y方向的起点 参数范围 [0, height-1]
    // 参数说明     dat             需要显示的变量 数据类型 double
    // 参数说明     num             整数位显示长度   最高8位
    // 参数说明     pointnum        小数位显示长度   最高6位
    // 返回参数     void
    // 使用示例     ips200.show_float(0, 0, x, 2, 3);
    // 备注信息     特别注意当发现小数部分显示的值与你写入的值不一样的时候，可能是由于浮点数精度丢失问题导致的，这并不是显示函数的问题，有关问题的详情，请自行百度学习   浮点数精度丢失问题。负数会显示一个 '-'号
    //-------------------------------------------------------------------------------------------------------------------
    void show_float(uint16 x, uint16 y, const double dat, uint8 num, uint8 pointnum);

    void print(uint16 x, uint16 y, const char* fmt, ...);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示灰度图像
    // 参数说明     x               坐标x方向的起点 [0, width-1]
    // 参数说明     y               坐标y方向的起点 [0, height-1]
    // 参数说明     image           灰度图像数据首地址 数据类型 const uint8*
    // 参数说明     width           图像宽度 像素
    // 参数说明     height          图像高度 像素
    // 返回参数     void
    // 使用示例     ips200.show_gray_image(0,0,gray_buf,100,80);
    // 备注信息     灰度值自动转换为RGB565格式显示，灰度值范围0~255；优化为批量转换和复制
    //-------------------------------------------------------------------------------------------------------------------
    void show_gray_image(uint16 x, uint16 y, const uint8 *image, uint16 width, uint16 height);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示RGB565格式图像
    // 参数说明     x               坐标x方向的起点 [0, width-1]
    // 参数说明     y               坐标y方向的起点 [0, height-1]
    // 参数说明     image           RGB565图像数据首地址 数据类型 const uint16*
    // 参数说明     width           图像宽度 像素
    // 参数说明     height          图像高度 像素
    // 返回参数     void
    // 使用示例     ips200.show_rgb_image(0,0,rgb_buf,100,80);
    // 备注信息     直接写入RGB565格式数据到缓冲区，无逐点转换，速度更快；优化为整行复制
    //-------------------------------------------------------------------------------------------------------------------
    void show_rgb_image(uint16 x, uint16 y, const uint16 *image, uint16 width, uint16 height);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示屏初始化函数
    // 参数说明     path            framebuffer设备节点路径 如 "/dev/fb0"
    // 返回参数     void
    // 使用示例     ips200.init("/dev/fb0");
    // 备注信息     打开fb设备、获取屏幕参数、映射显存、分配缓冲区、初始化清屏，程序初始化阶段只调用一次
    //-------------------------------------------------------------------------------------------------------------------
    void init(const char *path = FB_PATH, uint8 is_reload_driver = 1);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     更新屏幕显示
    // 参数说明     void
    // 返回参数     void
    // 使用示例     ips200.update();
    // 备注信息     将缓冲区中的脏区域数据批量复制到显存，并重置脏区域。应在绘制完成后调用。
    //-------------------------------------------------------------------------------------------------------------------
    void update(void);

};

#endif