/*********************************************************************************************************************
* UDP发送模块 - 头文件
* 功能：通过UDP协议发送JPEG图像或原始数据
* 适配：LS2K0300开源库
********************************************************************************************************************/

#ifndef _UDP_SENDER_HPP_
#define _UDP_SENDER_HPP_

#include <opencv2/opencv.hpp>
#include "zf_common_typedef.hpp"
// UDP服务器配置 - 修改为你电脑的IP地址
#define UDP_SERVER_IP   "192.168.2.49"
#define UDP_SERVER_PORT 8080

// JPEG压缩质量配置 (1-100)
#define JPEG_QUALITY    40

// 图像发送模式
enum ImageSendMode
{
    MODE_COLOR = 0,         // 彩色图像模式（默认）
    MODE_BINARY = 1,        // 二值化图像模式
    MODE_BINARY_EDGE = 2    // 二值化+赛道边界模式（分离发送）
};

// 边界数据包格式（用于UDP传输）
#pragma pack(push, 1)  // 1字节对齐
struct edge_data_packet_t
{
    uint8  header[4];           // 数据包头 "EDGE"
    uint16 image_width;         // 图像宽度
    uint16 image_height;        // 图像高度
    int16  left_line[120];      // 左边界数组（最大支持120行）
    int16  mid_line[120];       // 中线数组
    int16  right_line[120];     // 右边界数组
    uint16 checksum;            // 校验和
};
#pragma pack(pop)

// 原始二值化图像包格式（用于UDP传输）
#pragma pack(push, 1)
struct raw_image_packet_t
{
    uint8  header[4];           // 数据包头 "BIMG"
    uint16 image_width;         // 图像宽度
    uint16 image_height;        // 图像高度
    // 图像数据紧随其后（不在结构体中，动态大小）
};
#pragma pack(pop)

// UDP发送类（统一管理图像和数据发送）
class udp_sender
{
private:
    int udp_socket_fd;                      // UDP socket文件描述符
    struct sockaddr_in udp_server_addr;     // UDP服务器地址结构
    bool is_enabled;                        // 使能标志
    ImageSendMode send_mode;                // 图像发送模式
    
    // JPEG编码参数和缓冲区（init时创建，避免每帧分配）
    std::vector<int> jpeg_params;
    std::vector<uchar> jpeg_buffer;
    
    // 常量配置
    static const int MAX_UDP_PACKET = 65507;  // UDP最大包大小

public:
    udp_sender();
    ~udp_sender();

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介 初始化UDP发送器
    // 参数说明 server_ip    服务器IP地址
    // 参数说明 port         服务器端口号
    // 返回参数 int8         0-成功  -1-失败
    //-------------------------------------------------------------------------------------------------------------------
    int8 init(const char* server_ip, int port);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介 发送cv::Mat图像（JPEG压缩后发送）
    // 参数说明 image        OpenCV Mat图像对象
    // 返回参数 bool         true-发送成功  false-发送失败
    //-------------------------------------------------------------------------------------------------------------------
    bool send_image(const cv::Mat& image);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介 发送原始数据（如角点结构体）
    // 参数说明 data         数据指针
    // 参数说明 length       数据长度（字节）
    // 返回参数 bool         true-发送成功  false-发送失败
    //-------------------------------------------------------------------------------------------------------------------
    bool send_data(const void* data, size_t length);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介 发送原始二值化图像（带包头，不压缩）
    // 参数说明 image_data   图像数据指针
    // 参数说明 width        图像宽度
    // 参数说明 height       图像高度
    // 返回参数 bool         true-发送成功  false-发送失败
    //-------------------------------------------------------------------------------------------------------------------
    bool send_raw_image(const uint8* image_data, int width, int height);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介 关闭UDP连接
    //-------------------------------------------------------------------------------------------------------------------
    void close();

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介 获取使能状态
    //-------------------------------------------------------------------------------------------------------------------
    bool is_enable() const;

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介 设置图像发送模式
    // 参数说明 mode         发送模式（MODE_COLOR/MODE_BINARY/MODE_BINARY_EDGE）
    //-------------------------------------------------------------------------------------------------------------------
    void set_send_mode(ImageSendMode mode);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介 获取当前发送模式
    // 返回参数 ImageSendMode 当前模式
    //-------------------------------------------------------------------------------------------------------------------
    ImageSendMode get_send_mode() const;

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介 处理单帧图像 - 纯图传功能（无检测）
    // 功能说明 根据发送模式发送不同格式的图像：
    //         MODE_COLOR: 发送彩色JPEG图像
    //         MODE_BINARY: 发送二值化图像（需提前设置 user_binary_image 等）
    //         MODE_BINARY_EDGE: 发送二值化图像+边界数据
    // 参数说明 color_frame  外部传入的彩色图像帧（由调用方负责采集）
    //                       MODE_BINARY/BINARY_EDGE 且用户已设置 user_binary_image 时可传空 Mat
    // 返回参数 int8         0=成功  -1=失败
    //-------------------------------------------------------------------------------------------------------------------
    int8 process_frame(const cv::Mat& color_frame);
};

#endif // _UDP_SENDER_HPP_
